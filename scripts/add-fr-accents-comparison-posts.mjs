function applySubs(content) {
  let out = content
  for (const [from, to] of SUBS) {
    // Word boundary to avoid partial replacements
    const re = new RegExp(`\\b${from}\\b`, 'g')
    out = out.replace(re, to)
  }
  return out
}

let totalChanges = 0
for (const rel of FILES) {
  const path = join(ROOT, rel)
  const before = readFileSync(path, 'utf-8')
  const after = applySubs(before)
  const changed = before !== after
  const beforeChars = before.length
  const afterChars = after.length
  if (changed) {
    writeFileSync(path, after)
    totalChanges++
    console.log(`  modified ${rel} (+${afterChars - beforeChars} bytes)`)
  } else {
    console.log(`  unchanged ${rel}`)
  }
}
console.log(`\nDone. ${totalChanges}/${FILES.length} files modified.`)
